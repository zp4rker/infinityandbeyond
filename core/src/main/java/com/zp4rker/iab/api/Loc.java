package com.zp4rker.iab.api;

public class Loc {
    private final int x, y, z;

    public Loc(int x, int y, int z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getZ() {
        return z;
    }
}
